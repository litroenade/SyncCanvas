function getMessageValue(
  locale: AppLocale,
  key: string,
): TranslationValue | undefined {
  return MESSAGES[locale][key] ?? MESSAGES[DEFAULT_LOCALE][key];
}

export function normalizeLocale(input?: string | null): AppLocale {
  const value = input?.trim().toLowerCase();
  if (!value) {
    return DEFAULT_LOCALE;
  }
  if (value.startsWith('zh')) {
    return 'zh-CN';
  }
  return 'en-US';
}

export function resolveInitialLocale(): AppLocale {
  if (typeof window === 'undefined') {
    return DEFAULT_LOCALE;
  }

  try {
    const stored = window.localStorage.getItem('sync-canvas-locale');
    if (stored) {
      return normalizeLocale(stored);
    }
  } catch {
    // Ignore localStorage access errors and fall back to browser language.
  }

  return normalizeLocale(window.navigator.language);
}

export function translateWithLocale(
  locale: AppLocale,
  key: string,
  params: TranslationParams = {},
): string {
  const value = getMessageValue(locale, key);
  if (typeof value === 'function') {
    return value(params);
  }
  return value ?? key;
}

export function getExcalidrawLanguage(locale: AppLocale): 'zh-CN' | 'en-US' {
  return locale === 'zh-CN' ? 'zh-CN' : 'en-US';
}

export function formatRelativeTimeLabel(
  locale: AppLocale,
  timestamp: number,
): string {
  const diff = Date.now() - timestamp;
  const minutes = Math.floor(diff / (60 * 1000));
  const hours = Math.floor(diff / (60 * 60 * 1000));
  const days = Math.floor(diff / (24 * 60 * 60 * 1000));

  if (minutes < 1) {
    return translateWithLocale(locale, 'history.justNow');
  }
  if (minutes < 60) {
    return translateWithLocale(locale, 'history.minutesAgo', { count: minutes });
  }
  if (hours < 24) {
    return translateWithLocale(locale, 'history.hoursAgo', { count: hours });
  }
  if (days < 7) {
    return translateWithLocale(locale, 'history.daysAgo', { count: days });
  }
  return new Intl.DateTimeFormat(locale).format(new Date(timestamp));
}

export function formatDateTimeLabel(
  locale: AppLocale,
  timestamp: number,
): string {
  return new Intl.DateTimeFormat(locale, {
    dateStyle: 'medium',
    timeStyle: 'short',
  }).format(new Date(timestamp));
}
